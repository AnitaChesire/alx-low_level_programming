#include <stdlib.h>

/**
 * _div - divide integers
 * @a: first int
 * @b: second int
 * Return: division 
 */

int _div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus of integers
 * @a: first int
 * @b: second int
 * Return: modulus 
 */

int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - multiply integers
 * @a: first int
 * @b: second int
 * Return: multiply
 */

int mul(int a, int b)
{
	return (a * b);
}


/**
 * add - add integers
 * @a: first int
 * @b: second int
 * Return: addition 
 */

int add(int a, int b)
{
	return (a + b);
}


/**
 * sub - add integers
 * @a: first int
 * @b: second int
 * Return: subtraction
 */

int sub(int a, int b)
{
	return (a - b);
}
